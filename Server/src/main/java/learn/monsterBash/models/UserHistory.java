package learn.monsterBash.models;

public class UserHistory {

    long userWins;

    long userLosses;


    public long getUserWins() {
        return userWins;
    }

    public void setUserWins(long userWins) {
        this.userWins = userWins;
    }

    public long getUserLosses() {
        return userLosses;
    }

    public void setUserLosses(long userLosses) {
        this.userLosses = userLosses;
    }
}
